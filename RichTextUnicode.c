#include <stdio.h>
#include <stdlib.h>
#include <wchar.h>
#include <locale.h>

int main(int argc, char **argv) {
	wchar_t c;
	int A, a, zero, t = atoi(argv[1]);

	// courtesy: https://stackoverflow.com/a/43834410/1429450
	setlocale(LC_CTYPE, "");

	// offsets courtesy: https://yaytext.com/
	switch (t) {
		case 0: // bold serif
			A = 0x1d400;
			a = 0x1d41a;
			zero = 0x1d7ce;
			break;
		case 1: // bold sans
			A = 0x1d5d4;
			a = 0x1d5ee;
			zero = 0x1d7ec;
			break;
		case 2: // italic serif
			A = 0x1d434;
			a = 0x1d44e;
			zero = '0';
			break;
		case 3: // italic sans
			A = 0x1d608;
			a = 0x1d622;
			zero = '0';
			break;
		case 4: // bold italic serif
			A = 0x1d468;
			a = 0x1d482;
			zero = '0';
			break;
		case 5: // bold italic sans
			A = 0x1d63c;
			a = 0x1d656;
			zero = '0';
			break;
		case 6: // script
			A = 0x1d49c;
			a = 0x1d4b6;
			zero = '0';
			break;
		case 7: // bold script
			A = 0x1d4d0;
			a = 0x1d4ea;
			zero = '0';
			break;
		case 8: // double-struck
			A = 0x1d538;
			a = 0x1d552;
			zero = 0x1d7d8;
			break;
		case 9: // monospace
			A = 0x1d670;
			a = 0x1d68a;
			zero = '0';
			break;
		case 10: // fraktur
			A = 0x1d504;
			a = 0x1d51e;
			zero = '0';
			break;
		case 11: // bold fraktur
			A = 0x1d56c;
			a = 0x1d586;
			zero = '0';
			break;
		case 12: // full width
			A = 0xff21;
			a = 0xff41;
			zero = 0xff10;
			break;
		case 13: // bubble text
			A = 0x24b6;
			a = 0x24d0;
			zero = 0x2460;
			break;
		case 14: // black bubble text
			A = 0x1f150;
			a = 0x1f150;
			zero = 0x2775;
			break;
		case 15: // square
			A = 0x1f130;
			a = 0x1f130;
			zero = '0';
			break;
		case 16: // parenthesis
			A = 0x1f110;
			a = 0x249c;
			zero = 0x2473;
			break;
	}

	while (wscanf(L"%lc", &c) != EOF)
		if (c >= 'A' && c <= 'Z') // uppercase
			wprintf(L"%lc", A + c - 'A');
		else if (c >= 'a' && c <= 'z') // lowercase
			wprintf(L"%lc", a + c - 'a');
		else if (c >= '0' && c <= '9') // numerals
			wprintf(L"%lc", zero + c - '0');
		else // everything else
			wprintf(L"%lc", c);

	return 0;
}

